    src = letterforms['normal'] # Translate out of this letterform
    while ui:=input("Translate glyphs >"):
        transform_into = lambda dst: ''.join(transform_char(src,dst,c) for c in ui)
        for name,form in letterforms.items():
            if form!=src: print(f"{name.capitalize()}: {transform_into(form)}")
        print()


### FUNCTIONS

def transform_char(source, destination, character):
    """Translates a character from one letterform to another, as given by two strings containing their respective available characters."""
    if len(source)!=len(destination): raise ValueError("`source` and `destination` must be of equal length")
    try:
        return destination[source.index(character)]
    except ValueError:
        return '_'


